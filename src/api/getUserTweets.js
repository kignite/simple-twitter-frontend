import axios from "axios";

const baseURL = "https://calm-basin-50282.herokuapp.com/api";

export const getUserTweets = async ({ id, token }) => {
  try {
    const data = await axios.get(`${baseURL}/users/${id}/tweets`, {
      headers: {
        Authorization: 'Bearer ' + token,
      },
    });
    return data;
  } catch (error) {
    console.log(error);
  }
};

